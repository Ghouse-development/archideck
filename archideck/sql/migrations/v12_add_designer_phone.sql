BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'designers' AND column_name = 'phone'
  ) THEN
    ALTER TABLE designers ADD COLUMN phone TEXT;
  END IF;
END $$;

